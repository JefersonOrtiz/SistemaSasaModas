    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlteracao = "alterar";
        habilitarDesabilitarCampos(true);
        int linha = tblProduto.getSelectedRow();
        try {
            int codigoProduto = (int) this.tblProduto.getValueAt(linha, 0);
            // Recuperar dados do banco
            modelProdutos = controllerProdutos.retornarProdutoController(codigoProduto);
            // Setar na interface
            this.txtCodigo.setText(String.valueOf(modelProdutos.getIdProduto()));
            this.txtDescricao.setText(modelProdutos.getNomeProd());
            this.txtEstoque.setText(String.valueOf(modelProdutos.getEstoqProd()));
            this.txtValor.setText(String.valueOf(modelProdutos.getValorProd()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "AVISO:", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.tblProduto.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.tblProduto.setRowSorter(classificador);
        String texto = txtPesquisar.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_btnPesquisarActionPerformed

