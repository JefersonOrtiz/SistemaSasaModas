    private void salvarProduto() {
        // Salva um novo produto no Banco
        modelProdutos.setNomeProd(this.txtDescricao.getText());
        modelProdutos.setEstoqProd(Integer.parseInt(this.txtEstoque.getText()));
        modelProdutos.setValorProd(formatador.converterVirgulaParaPonto(this.txtValor.getText()));
        if (controllerProdutos.salvarProdutoController(modelProdutos) > 0) {
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
            this.preencherTabelaProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void alterarProduto() {
        // Alterar um produto no Banco
        modelProdutos.setNomeProd(this.txtDescricao.getText());
        modelProdutos.setEstoqProd(Integer.parseInt(this.txtEstoque.getText()));
        modelProdutos.setValorProd(formatador.converterVirgulaParaPonto(this.txtValor.getText()));
        if (controllerProdutos.alterarProdutoController(modelProdutos)) {
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
            this.preencherTabelaProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar produto!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Habilitar e desabilitar os campos do formulários
     *
     * @param condicao
     */
    private void habilitarDesabilitarCampos(boolean condicao) {
        txtDescricao.setEnabled(condicao);
        txtEstoque.setEnabled(condicao);
        txtValor.setEnabled(condicao);
        btnSalvar.setEnabled(condicao);
    }

    /**
     * Limpar os campos do formulários
     */
    private void limparCampos() {
        txtDescricao.setText("");
        txtEstoque.setText("");
        txtValor.setText("");
    }

    /**
     * Preenche a tabela com os produtos cadastrados
     */
    private void preencherTabelaProdutos() {
        listaModelProdutos = controllerProdutos.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) tblProduto.getModel();
        modelo.setNumRows(0);
        //inserir produtos na tabela
        int cont = listaModelProdutos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelProdutos.get(i).getIdProduto(),
                listaModelProdutos.get(i).getNomeProd(),
                listaModelProdutos.get(i).getEstoqProd(),
                listaModelProdutos.get(i).getValorProd()
            });
        }
    }

