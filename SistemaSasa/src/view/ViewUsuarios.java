    // Limpar formulário
    private void limparCampos() {
        txtCodigo.setText("");
        txtUsuario.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
    }

    // Habilitar e desabiliitar campos do formulário
    private void habilitarDesabilitarCampos(boolean condicao) {
        txtUsuario.setEditable(condicao);
        txtLogin.setEditable(condicao);
        txtSenha.setEditable(condicao);
        btnSalvar.setEnabled(condicao);
    }

    /**
     * Carregar usuários na tabela
     */
    private void preencherTabelaUsuario() {
        listaModelUsuarios = controllerUsuarios.getListaUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setNumRows(0);
        //inserir usuários na tabela
        int cont = listaModelUsuarios.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelUsuarios.get(i).getIdUsuario(),
                listaModelUsuarios.get(i).getNomeUsuario(),
                listaModelUsuarios.get(i).getLoginUsuario(),});
        }
    }

