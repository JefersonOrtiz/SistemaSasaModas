    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            modelUsuarios.setIdUsuario(Integer.parseInt(txtCodigo.getText()));
        } catch (NumberFormatException e) {
        }
        modelUsuarios.setNomeUsuario(txtUsuario.getText());
        modelUsuarios.setLoginUsuario(txtLogin.getText());
        modelUsuarios.setSenhaUsuario(txtSenha.getText());

        if (salvarAlteracao.equals("salvar")) {
            if (controllerUsuarios.salvarUsuariosController(modelUsuarios) > 0) {
                JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                this.preencherTabelaUsuario();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            if (controllerUsuarios.atualizarUsuariosController(modelUsuarios)) {
                JOptionPane.showMessageDialog(this, "Usuário alterado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                this.preencherTabelaUsuario();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        // Exclui um usuário do Banco
        int linha = tblUsuarios.getSelectedRow();
        int codigoUsuario = (int) tblUsuarios.getValueAt(linha, 0);

        if (controllerUsuarios.excluirUsuariosController(codigoUsuario)) {
            JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
            this.preencherTabelaUsuario();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        // Altera um usuário no Banco
        int linha = tblUsuarios.getSelectedRow();
        int codigoUsuario = (int) tblUsuarios.getValueAt(linha, 0);

        modelUsuarios = controllerUsuarios.getUsuariosController(codigoUsuario);
        txtCodigo.setText(String.valueOf(modelUsuarios.getIdUsuario()));
        txtUsuario.setText(modelUsuarios.getNomeUsuario());
        txtLogin.setText(modelUsuarios.getLoginUsuario());
        txtSenha.setText(modelUsuarios.getSenhaUsuario());

        salvarAlteracao = "alterar";
        habilitarDesabilitarCampos(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

