    /**
     * Preencher tabela de vendas
     */
    private void preencherVendas() {
        DefaultTableModel modelo = (DefaultTableModel) tbVendas.getModel();
        listaModelVendasClientes = controllerVendasCliente.getListaVendasClienteController();

        int cont = listaModelVendasClientes.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelVendasClientes.get(i).getModelVendas().getIdVenda(),
                listaModelVendasClientes.get(i).getModelClientes().getNomeCliente(),
                listaModelVendasClientes.get(i).getModelVendas().getDataVenda()
            });
        }
    }

    private void preencherCodigoClientePeloCombobox() {
        modelClientes = controllerClientes.getClientesController(cmbCliente.getSelectedItem().toString());
        txtCodCliente.setText(String.valueOf(modelClientes.getIdCliente()));
    }

    private void preencherCodigoProdutoPeloCombobox() {
        modelProdutos = controllerProdutos.retornarProdutoController(cmbProduto.getSelectedItem().toString());
        txtCodProd.setText(String.valueOf(modelProdutos.getIdProduto()));
    }

    /**
     * Calcula valor total com desconto
     */
    private void aplicarDesconto() {
        try {
            txtValorTot.setText(String.valueOf(
                    Double.parseDouble(txtValorTot.getText()) - Double.parseDouble(txtDesc.getText())));
        } catch (NumberFormatException e) {
        }
    }

    /**
     * Soma o valor de todas as vendas
     */
    private void somarValorTotalProdutos() {
        double soma = 0, valor;
        int cont = tbVendaProd.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) tbVendaProd.getValueAt(i, 4);
            soma = soma + valor;
        }
        txtValorTot.setText(String.valueOf(soma));
        aplicarDesconto();
    }

    /**
     * Limpar Formulário de vendasProduto
     */
    private void limparFormulario() {
        txtQtd.setText("");
        txtDesc.setText("");
        txtValorTot.setText("");
        DefaultTableModel modelo = (DefaultTableModel) tbVendaProd.getModel();
        modelo.setNumRows(0);
    }

