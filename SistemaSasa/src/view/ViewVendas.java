    /**
     * Preencher combobox com os clientes cadastrados
     */
    private void listarClientes() {
        listaModelClientes = controllerClientes.getListaClientesController();
        cmbCliente.removeAllItems();
        for (int i = 0; i < listaModelClientes.size(); i++) {
            cmbCliente.addItem(listaModelClientes.get(i).getNomeCliente());
        }
    }

    /**
     * Preencher combobox com os produtos do estoque
     */
    private void listarProdutos() {
        listaModelProdutos = controllerProdutos.retornarListaProdutoController();
        cmbProduto.removeAllItems();
        for (int i = 0; i < listaModelProdutos.size(); i++) {
            cmbProduto.addItem(listaModelProdutos.get(i).getNomeProd());
        }
    }

