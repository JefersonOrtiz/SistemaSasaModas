    private void btnAddProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdActionPerformed
        // TODO add your handling code here:
        if (txtQtd.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Você deve preencher todos os campos!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
        } else {
            modelProdutos = controllerProdutos.retornarProdutoController(Integer.parseInt(txtCodProd.getText()));
            // Adicionar uma linha na tabela vendaProd
            DefaultTableModel modelo = (DefaultTableModel) tbVendaProd.getModel();
            int cont = 0;
            double qtd = 0;
            qtd = Double.parseDouble(txtQtd.getText());
            for (int i = 0; i < cont; i++) {
                modelo.setNumRows(0);
            }

            modelo.addRow(new Object[]{
                modelProdutos.getIdProduto(),
                modelProdutos.getNomeProd(),
                txtQtd.getText(),
                modelProdutos.getValorProd(),
                qtd * modelProdutos.getValorProd()
            });
            somarValorTotalProdutos();
        }
    }//GEN-LAST:event_btnAddProdActionPerformed

